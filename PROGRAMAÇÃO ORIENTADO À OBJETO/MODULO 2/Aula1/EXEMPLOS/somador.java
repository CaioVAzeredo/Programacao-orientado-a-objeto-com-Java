package EXEMPLOS;
public class somador{
    public static void main(String [] args){
        double valor1 = 10;
        double valor2 = 4;
        double retorno = somar(valor1, valor2);
        System.out.println(retorno);
    }
    public static double somar(double param1, double param2){
        return param1 + param2;
             
        
    }
}